#pragma once

namespace SPEngine
{
	///////////////////////////////////////////////////////////////////////
	/// @brief SPIterator to go through a container.
	///
	///////////////////////////////////////////////////////////////////////
	template <typename T>
	class SPIterator
	{
	public:
		virtual void First() = 0;
		virtual void Next() = 0;
		virtual bool IsDone() const = 0;
		virtual T CurrentItem() const = 0;
	protected:
		SPIterator() {}
	};
}


